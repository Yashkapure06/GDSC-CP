package Day1;

public class ArithmeticOperations {

	public static void main(String[] args) {
		// Write Simple Program using Arithmetic operators
		
		int i = 10;	
		System.out.println(i); // 10
		int i2 = ++i;	
		System.out.println(i2); //11
		System.out.println("---");
		System.out.println(i+i2);
		System.out.println(i-i2);
		System.out.println(i*i2);
		System.out.println(i/i2);
		System.out.println(i%i2);
	}

}
