package Day1_18_09;

public class ComparisonOperations {

	public static void main(String[] args) {
		//  Write a Simple Program using Comparison Operators.
		int i1 = 10;
		int i2 = 20;
		
		System.out.println(i1 == i2);	// false
		System.out.println(i1 != i2);	// true
		System.out.println(i1 > i2);	// false
		System.out.println(i1 < i2);	// true
		System.out.println(i1 >= i2);	// false
		System.out.println(i1 <= i2);	// true
		
	}

}
