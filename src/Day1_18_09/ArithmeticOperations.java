package Day1_18_09;

public class ArithmeticOperations {

	public static void main(String[] args) {
		// Write Simple Program using Arithmetic operators
		
		int i = 10;	
		int i2 = 15;
		
		System.out.println("---");
		System.out.println(i+i2);// 25
		System.out.println(i-i2);// -5
		System.out.println(i*i2);// 150
		System.out.println(i/i2);// 0
		System.out.println(i%i2);// 10
	}

}
